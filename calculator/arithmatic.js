let Multiplication = (num1, num2) => {
    return num1 * num2;
};
let Division = (num1, num2) => {
    return num1 / num2;
};
let Addition = (num1, num2) => {
    return num1 + num2;
};
let Substraction = (num1, num2) => {
    return num1 - num2;
};
export { Multiplication, Addition, Division, Substraction };
